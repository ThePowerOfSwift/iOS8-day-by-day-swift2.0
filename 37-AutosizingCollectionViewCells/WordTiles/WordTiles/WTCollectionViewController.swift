import UIKit

private let reuseIdentifier = "Cell"

class WTCollectionViewController: UICollectionViewController {
    let wordList = WordList()


    override func viewDidLoad() {
        super.viewDidLoad()
        if let cvl = collectionViewLayout as? UICollectionViewFlowLayout{
            cvl.estimatedItemSize = CGSize(width: 150, height: 2)
        }
    }

    // MARK: UICollectionViewDataSource

    override func numberOfSectionsInCollectionView(collectionView: UICollectionView) -> Int {
        return 1
    }


    override func collectionView(collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return wordList.words.count
    }

    override func collectionView(collectionView: UICollectionView, cellForItemAtIndexPath indexPath: NSIndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCellWithReuseIdentifier("Cell", forIndexPath: indexPath) as! WordTileCollectionViewCell
        
        cell.word = wordList.words[indexPath.item]
    
        return cell
    }

